package SoloLearnJava.ClassesAndObjects.ClassAttributes;

//Let's create a class called Vehicle, with its corresponding attributes and methods.
public class Vehicle {
    int maxSpeed;
    int wheels;
    String color;
    double fuelCapacity;
    void horn() {
        System.out.println("Beep!");
    }
    /*maxSpeed, wheels, color and fuelCapacity are the
     attributes of our Vehicle class, and horn() is the only method.*/
}
