package SoloLearnJava.ClassesAndObjects.Module4Quiz;

public class Quiz2 {
    //Fill in the blanks to create a method that returns the minimum of the two parameters.
    public int minFunc(int n1, int n2) {
        int min;
        if (n1 > n2)
            min = n2;
        else
            min = n1;
        return min;
    }
}
