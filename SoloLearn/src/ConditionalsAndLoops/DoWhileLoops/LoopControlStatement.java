package ConditionalsAndLoops.DoWhileLoops;

public class LoopControlStatement {
    public static void main(String[] args) {
        //Fill in the blanks to print the values of the x variable 5 times.
        int x = 1;

        do {
            System.out.println(x);
            x++;
        }
        while (x <= 5);
    }
}
